        <p>Conservaremos sus datos:</p>
        <ul>
            <li>Mientras dure la relación contractual</li>
            <li>Hasta 5 años para cumplir obligaciones legales (fiscales, comerciales)</li>
            <li>Hasta que revoque su consentimiento para fines de marketing</li>
        </ul>
        
