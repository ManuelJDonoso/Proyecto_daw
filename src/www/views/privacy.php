            <li><strong>Datos técnicos:</strong> Dirección IP, tipo de navegador, versión, páginas visitadas, tiempo de visita.</li>
            <li><strong>Datos de uso:</strong> Cómo interactúa con nuestro sitio (clickstream, heatmaps).</li>
            <li><strong>Cookies:</strong> Como se detalla en nuestra <a href="#cookies">Política de Cookies</a>.</li>
        </ul>
        
        <h2>2. Finalidad del Tratamiento</h2>
        <p>Usamos sus datos para:</p>
        <ul>
            <li>Gestionar consultas y prestar nuestros servicios</li>
            <li>Mejorar la experiencia del usuario en nuestro sitio web</li>
            <li>Análisis estadísticos anónimos</li>
            <li>Cumplir con obligaciones legales</li>
            <li>Enviar comunicaciones (solo con consentimiento explícito)</li>
        </ul>
        
        <h2 id="cookies">3. Cookies y Tecnologías Similares</h2>
        <p>Utilizamos cookies técnicas necesarias para el funcionamiento del sitio y cookies analíticas de Google Analytics (anonimizadas). Puede gestionar sus preferencias de cookies mediante el banner de consentimiento o configurando su navegador.</p>
        
        <h2>4. Bases Legales para el Tratamiento</h2>
        <p>El tratamiento se basa en:</p>
        <ul>
            <li>Ejecución de contratos (para servicios solicitados)</li>
            <li>Consentimiento explícito (para marketing)</li>
            <li>Interés legítimo (mejora del sitio web)</li>
            <li>Cumplimiento de obligaciones legales</li>
        </ul>
        
        <h2>5. Compartición de Datos</h2>
        <p>Sus datos podrán ser compartidos con:</p>
        <ul>
            <li>Proveedores de servicios técnicos (hosting, email)</li>
            <li>Google Analytics (datos anonimizados)</li>
            <li>Autoridades cuando sea requerido legalmente</li>
        </ul>
        <p>No realizamos transferencias internacionales de datos fuera de la UE/EEE.</p>
        
        <h2>6. Seguridad de los Datos</h2>
        <p>Implementamos medidas técnicas y organizativas adecuadas (SSL, encriptación, controles de acceso) para proteger sus datos contra pérdida, uso indebido o acceso no autorizado.</p>
        
